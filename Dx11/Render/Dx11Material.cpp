#include "Dx11Material.h"


//=================================================================================================
// @brief	Construct
//=================================================================================================
Dx11Material::Dx11Material()
: PixelShader ( nullptr )
, Texture2D   ( nullptr )
{
}

//=================================================================================================
// @brief	Initialize
//=================================================================================================
void Dx11Material::Initialize( Dx11PixelShader* InPixelShader, Dx11Texture2D* InTexture2D )
{
    PixelShader = InPixelShader;
    Texture2D   = InTexture2D;

    PixelShader->Render();
    Texture2D->Render();
}

