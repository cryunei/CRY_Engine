#include "Dx11Mesh.h"

//=================================================================================================
// @brief	Constructor
//=================================================================================================
Dx11Mesh::Dx11Mesh()
{
}

//=================================================================================================
// @brief	Initialize
//=================================================================================================
void Dx11Mesh::Initialize()
{
    Primitive.Initialize();
    Material .Initialize();

    // Test code
    Transform.SetLocationX( 1.f );
    Transform.SetRotationX( 1.f );
    Transform.SetScale( 3.f, 3.f, 3.f );
}

//=================================================================================================
// @brief	Render
//=================================================================================================
void Dx11Mesh::Render()
{
    Material .Render();
    Primitive.Render();
}
